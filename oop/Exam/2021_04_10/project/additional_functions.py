class Additional:
    @staticmethod
    def eat(size: int, increase: int) -> int:
        """
        Увеличава размера на рибата с дадена стойност.
        :param size: Текущият размер на рибата.
        :param increase: Колко да се увеличи размера.
        :return: Новият размер на рибата.
        """
        return size + increase
